package AST;

import Visitor.*;
import Types.*;
import SemanticException.*;
import Temps.*;

public class Expression extends ASTNode {
	
	public void accept (PrintVisitor v) {
		v.visit(this);
	}
	
	public Type accept (TypeVisitor v) throws SemanticException {
		return v.visit(this);
	}
	
	public Temp accept (IRVisitor v) {
		return v.visit(this);
	}
	
}